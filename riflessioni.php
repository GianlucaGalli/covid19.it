 scontato questi gesti ed il loro significato?<br><br>In una fase sociale in cui pensare al proprio orto è diventata la regola, il virus ci manda un messaggio chiaro: l'unico modo per uscirne è la reciprocità, il senso di appartenenza, la comunita, il sentire di essere parte di qualcosa di più grande di cui prendersi cura e che si può prendere cura di noi. La responsabilità condivisa, il sentire che dalle tue azioni dipendono le sorti non solo tue, ma di tutti quelli che ti circondano. E che tu dipendi da loro.<br><br>Allora, se smettiamo di fare la caccia alle streghe, di domandarci di chi è la colpa o perché è accaduto tutto questo, ma ci domandiamo cosa possiamo imparare da questo, credo che abbiamo tutti molto su cui riflettere ed impegnarci.<br>Perchè col cosmo e le sue leggi, evidentemente, siamo in debito spinto. Ce lo sta spiegando il virus, a caro prezzo."</p>            <p></p>
